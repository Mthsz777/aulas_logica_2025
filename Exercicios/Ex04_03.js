const prompt = require('prompt-sync')()
let mes = Number(prompt('Digite o numero do mês'))
switch(mes){
    case mes = 1:
        console.log("Janeiro")
        break
        case mes = 2:
            console.log("Fevereiro")
            break
            case mes = 3:
                console.log("Março")
                break
                case mes = 4:
                    console.log("Abril")
                    break
                    case mes = 5:
                        console.log("Maio")
                        break
                        case mes = 6:
                            console.log("junho")
                            break
                            case mes = 7:
                                console.log("julho")
                                break
                                case mes = 8:
                                    console.log("Agosto")
                                    break
                                    case mes = 9:
                                        console.log("Setembro")
                                        break
                                        case mes = 10:
                                            console.log("Outubro")
                                            break
                                            case mes = 11:
                                                console.log("Novembro")
                                                break
                                                default:
                                                    console.log("Dezembro")
}